package AidPackage;

/**
 * Class to replace the Rectangle in awt.
 * 
 * @author SidneyTTW
 * 
 */
public class MyRectangle {
  public int x;
  public int y;
  public int width;
  public int height;

  public MyRectangle(int x, int y, int width, int height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }

}
